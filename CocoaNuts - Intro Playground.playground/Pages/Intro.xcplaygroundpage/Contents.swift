//: ### What is this Playground for, and what's a Playground?
//:
//: A Playground, is a fun way to interactively learn Swift and test out new ideas. The code you type is immeadiatly evaluated and any output is shown on the right hand side of the split screen, which makes for an ideal learning environment.
//:
//: This Playground is meant to introduce programming to complete beginners, as well as provide people who already have some coding experience with the fundamentals of the Swift Language. So, you **don't need any previous coding experience** to benifit from this Playground.
//:
//: ----
//: ### Structure of this Playground
//:
//: It is divided up into multiple pages, and each page covers a specific topic.
//:
//: The Playground was designed using something called the **Parteo Principle** (aka: 80/20 rule): To learn a new skill in the most effective and efficient manner, learn the small (20%) set of core topics that are used the most (80%) in everyday cases. So you won't learn everything, but you will walk away with the things that you will probably be using most.
//:
